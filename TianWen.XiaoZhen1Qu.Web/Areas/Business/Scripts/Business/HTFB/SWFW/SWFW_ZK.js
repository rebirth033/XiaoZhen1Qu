//加载商务服务_制卡基本信息
function LoadSWFW_ZKJBXX() {
    $.ajax({
        type: "POST",
        url: getRootPath() + "/Business/SWFW/LoadSWFW_ZKJBXX",
        dataType: "json",
        data:
        {
            ID: getUrlParam("ID")
        },
        success: function (xml) {
            if (xml.Result === 1) {
                var jsonObj = new JsonDB("myTabContent");
                jsonObj.DisplayFromJson("myTabContent", xml.Value.SWFW_ZKJBXX);
                jsonObj.DisplayFromJson("myTabContent", xml.Value.JCXX);
                $("#ID").val(xml.Value.SWFW_ZKJBXX.ID);
                //设置编辑器的内容
                ue.ready(function () {
                    ue.setHeight(200);
                    ue.setContent(xml.Value.BCMSString);
                });
                $("#spanLB").html(xml.Value.SWFW_ZKJBXX.LB);
                $("#spanQY").html(xml.Value.SWFW_ZKJBXX.QY);
                $("#spanDD").html(xml.Value.SWFW_ZKJBXX.DD);
                LoadPhotos(xml.Value.Photos);
                LoadXL(xml.Value.SWFW_ZKJBXX.LB, xml.Value.SWFW_ZKJBXX.XL);
                SetDuoX("KX", xml.Value.SWFW_ZKJBXX.KX);
            }
        },
        error: function (XMLHttpRequest, textStatus, errorThrown) { //有错误时的回调函数

        }
    });
}
