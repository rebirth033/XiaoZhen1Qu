$(document).ready(function () {
    $("body").bind("click", function () { Close("_XZQ"); });
    LoadXXYL_YDJBJBXX();
    BindClick("LB");
});

//绑定下拉框鼠标点击样式
function BindClick(type) {
    $("#div" + type + "Span").click(function () {
        if (type === "LB") {
            LoadCODESByTYPENAME("夜店酒吧", "LB", "CODES_XXYL", Bind, "YDJBLB", "LB", "");
        }
        
    });
}
//加载休闲娱乐_夜店酒吧基本信息
function LoadXXYL_YDJBJBXX() {
    $.ajax({
        type: "POST",
        url: getRootPath() + "/Business/XXYL/LoadXXYL_YDJBJBXX",
        dataType: "json",
        data:
        {
            ID: getUrlParam("ID")
        },
        success: function (xml) {
            if (xml.Result === 1) {
                var jsonObj = new JsonDB("myTabContent");
                jsonObj.DisplayFromJson("myTabContent", xml.Value.XXYL_YDJBJBXX);
                jsonObj.DisplayFromJson("myTabContent", xml.Value.JCXX);
                $("#ID").val(xml.Value.XXYL_YDJBJBXX.ID);
                //设置编辑器的内容
                ue.ready(function () {
                    ue.setHeight(200);
                    ue.setContent(xml.Value.BCMSString);
                });
                $("#spanLB").html(xml.Value.XXYL_YDJBJBXX.LB);
                $("#spanQY").html(xml.Value.XXYL_YDJBJBXX.QY);
                $("#spanDD").html(xml.Value.XXYL_YDJBJBXX.DD);
                LoadPhotos(xml.Value.Photos);
            }
        },
        error: function (XMLHttpRequest, textStatus, errorThrown) { //有错误时的回调函数

        }
    });
}
